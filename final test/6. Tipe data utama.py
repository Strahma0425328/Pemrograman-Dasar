# integer
# Mendeklarasikan variabel integer
x = 42
y = -10

# Operasi matematis
hasil_penjumlahan = x + y  # 42 + (-10) = 32
hasil_perkalian = x * 2    # 42 * 2 = 84

# Menampilkan hasil
print("Hasil penjumlahan:", hasil_penjumlahan)  # Output: Hasil penjumlahan: 32
print("Hasil perkalian:", hasil_perkalian)      # Output: Hasil perkalian: 84



# float
# Mendeklarasikan variabel float
pi = 3.14159
harga = 99.99

# Operasi matematis
luas_lingkaran = pi * 2**2  # 3.14159 * 4 = 12.56636
total_harga = harga + 10.01  # 99.99 + 10.01 = 110.0

# Menampilkan hasil
print("Luas lingkaran:", luas_lingkaran)  # Output: Luas lingkaran: 12.56636
print("Total harga:", total_harga)        # Output: Total harga: 110.0



# string
# Mendeklarasikan variabel string
nama = "Alice"
pesan = "Halo, dunia!"

# Operasi string
gabung = nama + " " + pesan  # Menggabungkan string
panjang = len(gabung)        # Menghitung panjang string

# Menampilkan hasil
print("Pesan gabungan:", gabung)  # Output: Pesan gabungan: Alice Halo, dunia!
print("Panjang string:", panjang)  # Output: Panjang string: 20
